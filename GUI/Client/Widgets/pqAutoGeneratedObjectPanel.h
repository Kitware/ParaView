#ifndef _pqAutoGeneratedObjectPanel_h
#define _pqAutoGeneratedObjectPanel_h

#include "pqNamedObjectPanel.h"
class QGridLayout;

/// Auto-generated Widget which provides an editor for editing properties of a proxy
/// Widgets are created based on the types of the proxy properties
class pqAutoGeneratedObjectPanel : public pqNamedObjectPanel
{
  Q_OBJECT
public:
  /// constructor
  pqAutoGeneratedObjectPanel(QWidget* p);
  /// destructor
  ~pqAutoGeneratedObjectPanel();

  /// set the proxy to display properties for
  void setProxy(pqSMProxy proxy);
  
protected:

  /// populate this widget with widgets to represent the properties
  void createWidgets();
  /// delete the widgets representing properties
  void deleteWidgets();

  QGridLayout* PanelLayout;

};

#endif

